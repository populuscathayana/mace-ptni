#!/usr/bin/env python
"""Replot Pt(111) adatom PES results from an existing CSV.

This script does not run MACE. It only reads the CSV generated by
pt111_adatom_pes_mace.py, writes static 2D figures with consistent colorbar
ranges, and creates a Plotly-based HTML page with selectable 3D surfaces.
"""

from __future__ import annotations

import argparse
import csv
import html
