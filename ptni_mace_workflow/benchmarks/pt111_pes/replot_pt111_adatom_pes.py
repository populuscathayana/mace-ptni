import math
from pathlib import Path


def to_float(value: str | None) -> float | None:
    if value in (None, "", "None"):
        return None
    try:
        return float(value)
    except ValueError:
        return None


def read_rows(path: Path) -> list[dict]:
    with path.open("r", newline="", encoding="utf-8") as handle:
        rows = list(csv.DictReader(handle))
    if not rows:
        raise SystemExit(f"No rows in CSV: {path}")
    return rows


def numeric_array(rows: list[dict], key: str) -> list[float]:
    values = [to_float(row.get(key)) for row in rows]
    if any(value is None for value in values):
        missing = sum(value is None for value in values)
        raise SystemExit(f"Column {key!r} has {missing} missing/non-numeric values")
    return [float(value) for value in values if value is not None]


def display_pair(
    mace: list[float],
    dft: list[float],
    shift_shared_min_to_zero: bool,
) -> tuple[list[float], list[float], float]:
    if not shift_shared_min_to_zero:
        return mace, dft, 0.0
    offset = min(min(mace), min(dft))
    return [value - offset for value in mace], [value - offset for value in dft], offset


def display_energy_label(energy_label: str, shift_shared_min_to_zero: bool) -> str:
    if not shift_shared_min_to_zero:
        return energy_label
    return f"{energy_label}, shifted so shared min = 0"


def build_triangulation(x: list[float], y: list[float]):
    import matplotlib.tri as mtri

    triang = mtri.Triangulation(x, y)
    triangles = triang.triangles.tolist()
    return triang, [int(t[0]) for t in triangles], [int(t[1]) for t in triangles], [int(t[2]) for t in triangles]


def draw_single_tricontour(
    ax,
    triang,
    x: list[float],
    y: list[float],
    z: list[float],
    title: str,
    vmin: float,
    vmax: float,
    cmap: str,
    top_labels: dict[str, str] | None = None,
):
    import numpy as np
    from matplotlib import colors

    fill_levels = np.linspace(vmin, vmax, 29)
    line_levels = np.linspace(vmin, vmax, 13)
    norm = colors.Normalize(vmin=vmin, vmax=vmax)
    contour = ax.tricontourf(triang, z, levels=fill_levels, cmap=cmap, norm=norm)
    ax.tricontour(triang, z, levels=line_levels, colors="k", linewidths=0.25, alpha=0.35)
    ax.scatter(x, y, c=z, cmap=cmap, norm=norm, s=13, edgecolor="k", linewidth=0.15)
    ax.set_aspect("equal", adjustable="box")
    ax.set_xlabel("adatom x (A)")
    ax.set_ylabel("adatom y (A)")
    ax.set_title(title)
    if top_labels:
        for label, text in top_labels.items():
            for xi, yi, row_label in zip(x, y, top_labels["_labels"]):
                if row_label == label:
                    ax.annotate(text, (xi, yi), xytext=(3, 3), textcoords="offset points", fontsize=7)
                    ax.scatter([xi], [yi], marker="x", s=45, color="white", linewidth=1.4)
                    ax.scatter([xi], [yi], marker="x", s=45, color="black", linewidth=0.7)
                    break
    return contour


def add_fixed_range_colorbar(fig, ax, vmin: float, vmax: float, cmap: str, label: str, **kwargs):
    import numpy as np
    from matplotlib import cm, colors

    norm = colors.Normalize(vmin=vmin, vmax=vmax)
    mappable = cm.ScalarMappable(norm=norm, cmap=cmap)
    mappable.set_array([])
    ticks = np.linspace(vmin, vmax, 7)
    cbar = fig.colorbar(mappable, ax=ax, ticks=ticks, **kwargs)
    cbar.set_label(label)
    cbar.ax.set_yticklabels([f"{tick:.0f}" if abs(vmax - vmin) >= 20 else f"{tick:.3g}" for tick in ticks])
    return cbar


def save_static_plots(
    rows: list[dict],
    out_dir: Path,
    name: str,
    top_n: int,
    mace_key: str,
    dft_key: str,
    diff_key: str,
    energy_label: str,
    title_prefix: str,
    shift_shared_min_to_zero: bool,
) -> list[Path]:
    import matplotlib.pyplot as plt
    import numpy as np

    labels = [row["label"] for row in rows]
    x = numeric_array(rows, "initial_cart_x_A")
    y = numeric_array(rows, "initial_cart_y_A")
    mace_raw = numeric_array(rows, mace_key)
    dft_raw = numeric_array(rows, dft_key)
    mace, dft, _ = display_pair(mace_raw, dft_raw, shift_shared_min_to_zero)
    diff = numeric_array(rows, diff_key)
    triang, _, _, _ = build_triangulation(x, y)
    colorbar_label = display_energy_label(energy_label, shift_shared_min_to_zero)

    common_vmin = min(min(mace), min(dft))
    common_vmax = max(max(mace), max(dft))
    diff_abs = max(abs(min(diff)), abs(max(diff)))
    diff_vmin = -diff_abs
    diff_vmax = diff_abs

    out_paths: list[Path] = []

    panel_path = out_dir / f"{name}_mace_dft_unified_panel.png"
    fig, axes = plt.subplots(1, 2, figsize=(12, 5.4), constrained_layout=True)
    draw_single_tricontour(axes[0], triang, x, y, mace, f"MACE {title_prefix}", common_vmin, common_vmax, "viridis")
    draw_single_tricontour(axes[1], triang, x, y, dft, f"DFT {title_prefix}", common_vmin, common_vmax, "viridis")
    add_fixed_range_colorbar(
        fig,
        axes,
        common_vmin,
        common_vmax,
        "viridis",
        f"{colorbar_label}, shared range",
        shrink=0.9,
        pad=0.02,
    )
    fig.savefig(panel_path, dpi=220)
    plt.close(fig)
    out_paths.append(panel_path)

    for key, values, title, suffix in [
        ("mace", mace, f"MACE {title_prefix}", "mace_pes_unified"),
        ("dft", dft, f"DFT {title_prefix}", "dft_pes_unified"),
    ]:
        fig, ax = plt.subplots(figsize=(6.2, 5.4), constrained_layout=True)
        draw_single_tricontour(ax, triang, x, y, values, title, common_vmin, common_vmax, "viridis")
        add_fixed_range_colorbar(fig, ax, common_vmin, common_vmax, "viridis", f"{colorbar_label}, shared range")
        path = out_dir / f"{name}_{suffix}.png"
        fig.savefig(path, dpi=220)
        plt.close(fig)
        out_paths.append(path)

    error_order = sorted(range(len(rows)), key=lambda idx: abs(diff[idx]), reverse=True)[:top_n]
    label_map = {"_labels": labels}
    for rank, idx in enumerate(error_order, start=1):
        label_map[labels[idx]] = str(rank)

    diff_path = out_dir / f"{name}_mace_minus_dft_diverging.png"
    fig, ax = plt.subplots(figsize=(6.5, 5.6), constrained_layout=True)
    draw_single_tricontour(
        ax,
        triang,
        x,
        y,
        diff,
        f"MACE minus DFT {title_prefix}",
        diff_vmin,
        diff_vmax,
        "coolwarm",
        top_labels=label_map,
    )
    add_fixed_range_colorbar(fig, ax, diff_vmin, diff_vmax, "coolwarm", f"MACE - DFT {energy_label}")
    fig.savefig(diff_path, dpi=220)
    plt.close(fig)
    out_paths.append(diff_path)

    hist_path = out_dir / f"{name}_error_histogram.png"
    fig, ax = plt.subplots(figsize=(6.5, 4.2), constrained_layout=True)
    ax.hist(diff, bins=36, color="tab:blue", edgecolor="white")
    ax.axvline(0.0, color="black", linewidth=1)
    ax.axvline(float(np.mean(diff)), color="tab:red", linestyle="--", label=f"mean={np.mean(diff):.1f} meV")
    ax.set_xlabel(f"MACE - DFT {energy_label}")
    ax.set_ylabel("grid points")
    ax.set_title("Relative PES error distribution")
    ax.legend()
    fig.savefig(hist_path, dpi=220)
    plt.close(fig)
    out_paths.append(hist_path)

    return out_paths


def metrics(
    rows: list[dict],
    mace_key: str,
    dft_key: str,
    diff_key: str,
    shift_shared_min_to_zero: bool = False,
) -> dict[str, float | int | str]:
    import numpy as np

    mace_raw = numeric_array(rows, mace_key)
    dft_raw = numeric_array(rows, dft_key)
    mace_display, dft_display, offset = display_pair(mace_raw, dft_raw, shift_shared_min_to_zero)
    mace_raw_array = np.asarray(mace_raw, dtype=float)
    dft_raw_array = np.asarray(dft_raw, dtype=float)
    mace = np.asarray(mace_display, dtype=float)
    dft = np.asarray(dft_display, dtype=float)
    diff = np.asarray(numeric_array(rows, diff_key), dtype=float)
    labels = [row["label"] for row in rows]
    return {
        "n_points": len(rows),
        "mace_range_meV": float(mace.max() - mace.min()),
        "dft_range_meV": float(dft.max() - dft.min()),
        "raw_shared_min_meV": float(min(mace_raw_array.min(), dft_raw_array.min())),
        "raw_shared_max_meV": float(max(mace_raw_array.max(), dft_raw_array.max())),
        "display_energy_offset_meV": float(offset),
        "shared_colorbar_min_meV": float(min(mace.min(), dft.min())),
        "shared_colorbar_max_meV": float(max(mace.max(), dft.max())),
        "diff_colorbar_absmax_meV": float(max(abs(diff.min()), abs(diff.max()))),
        "mae_meV": float(np.mean(np.abs(diff))),
        "rmse_meV": float(np.sqrt(np.mean(diff * diff))),
        "mean_error_meV": float(np.mean(diff)),
        "max_abs_error_meV": float(np.max(np.abs(diff))),
        "pearson_r": float(np.corrcoef(dft, mace)[0, 1]),
        "mace_min_label": labels[int(np.argmin(mace_raw_array))],
        "dft_min_label": labels[int(np.argmin(dft_raw_array))],
    }


def top_error_rows(rows: list[dict], top_n: int, diff_key: str) -> list[dict]:
    return sorted(
        rows,
        key=lambda row: abs(to_float(row.get(diff_key)) or 0.0),
        reverse=True,
    )[:top_n]


